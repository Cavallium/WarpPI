			int yA = (int) Math.floor(e.getY());
			int yB = (int) Math.ceil(e.getY());
			int x0 = (int) Math.floor(e.getX());
			double x1double = e.getX() + e.computeFutureDX(dt);
			int x1 = (int) Math.floor(x1double);
